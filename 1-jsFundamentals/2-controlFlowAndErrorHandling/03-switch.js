let friend = 'Brandon';

switch (friend) {
case "Kenn":
    console.log('Wanna play Catan?');
    break;
case 'Carolyn':
  console.log('Wanna go rock climbing');
  break;
case 'Tyler':
  console.log('Can I ask a question?');
  break;
case 'David':
  console.log('I remember doing this...');
  break;
default:
  console.log(`I'm sorry, ${friend}, but do I know you?`)
}

