// Assignment operator
var x = 5

// Addition assignment
x += 1 // x = x + 1

// Subtraction assignment
x -= 1 // x = x - 1

// Multiplication assignment
x *= 1 // x = x * 1

// Division assignment
x /= 1 // x = x / 1

// Remainder (or Modulus) assignment
x %= 1 // x = x % 1

// Exponential assignment - x to the power of 1 
x **= 1 // x = x ** 1

let x = 10 % 1;
console.log(x);
